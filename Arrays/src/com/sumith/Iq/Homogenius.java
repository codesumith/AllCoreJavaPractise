package com.sumith.Iq;

public class Homogenius {

	public static void main(String[] args) {
		
		int [] ar=new int[3];
		
		ar[0]=0;
		ar[1]=1;
		ar[2]=2;
		
		
		
		System.out.println(" Element at ar[0] : "+ar[0]);
		System.out.println(" Element at ar[1] : "+ar[1]);
		System.out.println(" Element at ar[2] : "+ar[2]);
	}

}
