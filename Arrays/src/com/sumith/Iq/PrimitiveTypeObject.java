package com.sumith.Iq;

public class PrimitiveTypeObject {

	
	public static void main(String[]args) {
		
		int[]arr=new int[3];
		System.out.println(arr.getClass().getName());
		System.out.println("------------------------------------------------------");
		
		float[] frr=new float[4];
		System.out.println(frr.getClass().getName());
		System.out.println("------------------------------------------------------");
		
		byte []arr1=new byte[3];
		System.out.println(arr1.getClass().getName());
		System.out.println("------------------------------------------------------");
		
		char[]arr2=new char[3];
		System.out.println(arr2.getClass().getName());
		System.out.println("------------------------------------------------------");
		
		short[]arr3=new short[3];
		System.out.println(arr3.getClass().getName());
		System.out.println("------------------------------------------------------");
		
		long[]arr4=new long[3];
		System.out.println(arr4.getClass().getName());
		System.out.println("------------------------------------------------------");
		
		double[]arr5=new double[3];
		System.out.println(arr5.getClass().getName());
		System.out.println("------------------------------------------------------");
		
		boolean[]arr6=new boolean[3];
		System.out.println(arr6.getClass().getName());
		System.out.println("------------------------------------------------------");
	}
}
