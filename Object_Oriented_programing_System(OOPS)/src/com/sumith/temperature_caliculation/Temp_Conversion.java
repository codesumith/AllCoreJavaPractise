package com.sumith.temperature_caliculation;

public class Temp_Conversion {
	
	
	public double celsiusToFahren(double cel) {
		
		double c=(cel*1.8)+32;
		
		
		
		
		
		return  c;
	}
	
	
public int  fahrenheitToCelsius(double fahren) {
		
		
		double f=(fahren-32)*5/9;
		
		
		
		return(int)  f;
	}
	
	

}
