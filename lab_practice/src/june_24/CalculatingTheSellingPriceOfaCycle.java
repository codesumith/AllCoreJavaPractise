package june_24;

public class CalculatingTheSellingPriceOfaCycle {

	public static void main(String[] args) {
		int PurchasedOldCycleFor=1200;
		int SpentForRepairs     =250;
		int Coloring            =350;
		int NewAccessories      =500;
		int ProfitOnSellingCycle=1500;
		int SellingPriceOfCycle =PurchasedOldCycleFor+SpentForRepairs+Coloring +NewAccessories+ProfitOnSellingCycle;
		System.out.println("SellingPriceOfCycle:"+SellingPriceOfCycle);
	}

}
